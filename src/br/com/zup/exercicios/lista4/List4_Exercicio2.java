package br.com.zup.exercicios.lista4;
/***2 - Faça um programa que mostra na tela os números de 100 a 1.
 **/

public class List4_Exercicio2 {
    public static void main(String[] args){

        int n = 100;

        while(n >=1){
            System.out.println(n);
            n--;
        }

    }
}
